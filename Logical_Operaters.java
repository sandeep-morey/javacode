package javapractice;

public class Logical_Operaters 
{

	public static void main(String args[])
	{
		System.out.println("Logical operaters in java");
	
		int a =10;
		int b=20;
		if(a==b && a==10)
		{
			System.out.println(1);
		}
		
		if(a==b || a==10)
		{
			System.out.println("2");
		}
	}
	
	
	
}
